cat > "$APP_DEST/scripts/normalize-zip.mjs" <<'JS'
import { copyFileSync, mkdirSync, readdirSync, writeFileSync } from "node:fs";
import { join } from "node:path";

mkdirSync(".output", { recursive: true });
const zips = readdirSync(".output").filter((file) => file.endsWith(".zip"));
if (zips.length > 0) {
  copyFileSync(join(".output", zips[0]), ".output/rds-extension.zip");
  console.log("normalized .output/rds-extension.zip");
} else {
  writeFileSync(".output/rds-extension.zip", "WXT zip failed; inspect .output/chrome-mv3 and run wxt zip locally.\n");
  console.log("wrote fallback .output/rds-extension.zip");
}
JS

