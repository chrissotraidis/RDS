cat > "$APP_DEST/preview-server.ts" <<'TS'
import { existsSync } from "node:fs";
import { join } from "node:path";

const port = Number(process.env.HOST_PORT || process.env.PORT || 4000);
const root = process.cwd();

function fileFor(pathname: string) {
  if (pathname === "/") return join(root, "preview/index.html");
  if (pathname === "/health.json") return join(root, "health.json");
  if (pathname === "/extension.zip") return join(root, ".output/rds-extension.zip");
  return join(root, pathname.replace(/^\//, ""));
}

function typeFor(pathname: string) {
  if (pathname.endsWith(".html")) return "text/html; charset=utf-8";
  if (pathname.endsWith(".css")) return "text/css; charset=utf-8";
  if (pathname.endsWith(".js") || pathname.endsWith(".tsx") || pathname.endsWith(".ts")) return "text/javascript; charset=utf-8";
  if (pathname.endsWith(".json")) return "application/json; charset=utf-8";
  if (pathname.endsWith(".zip")) return "application/zip";
  return "application/octet-stream";
}

Bun.serve({
  port,
  async fetch(req) {
    const url = new URL(req.url);
    const path = fileFor(url.pathname);
    if (!existsSync(path)) return new Response("Not found", { status: 404 });
    return new Response(Bun.file(path), { headers: { "content-type": typeFor(url.pathname) } });
  },
});

console.log(`extension preview listening on ${port}`);
TS

